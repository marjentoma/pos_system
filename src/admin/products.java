    public void displayProducts() {
        int ctr = 0;
        productsTable.setModel(new DefaultTableModel(null, new String[]{"Menu Id", "Menu Name", "Menu Type", "Availability", "Price"}));
        try {
            Class.forName("com.mysql.jdbc.Driver");//load the driver
            try ( java.sql.Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/system_pos", "root", ""))//establishes connection
            {
                java.sql.Statement stmt = (java.sql.Statement) con.createStatement(); //get the connection stream(connection port)
                ResultSet datas = stmt.executeQuery("SELECT * FROM menu");
                DefaultTableModel model = (DefaultTableModel) productsTable.getModel();
                while (datas.next()) {
                    ctr = 1;
                    model.addRow(new Object[]{datas.getInt("menu_id"), datas.getString("menu_name"), datas.getString("menu_type"), datas.getString("availability"), datas.getFloat("price")
                       });
                }
                if (ctr == 0) {
                    JOptionPane.showMessageDialog(null, "No data found!.", "Alert", JOptionPane.WARNING_MESSAGE);
                }
                con.close();
            }
        } catch (ClassNotFoundException | SQLException e) {
            System.out.println(e);
        };
    }

