    private void productsTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_productsTableMouseClicked
        DefaultTableModel model = (DefaultTableModel) productsTable.getModel();
        int selectedRowIndex = productsTable.getSelectedRow();

        idTextField.setText(model.getValueAt(selectedRowIndex, 0).toString());
        nameTextField.setText(model.getValueAt(selectedRowIndex, 1).toString());
        typeTextField.setSelectedItem(model.getValueAt(selectedRowIndex, 2));
        availabilityTextField.setSelectedItem(model.getValueAt(selectedRowIndex, 3));
        priceTextField.setText(model.getValueAt(selectedRowIndex, 4).toString());
    }//GEN-LAST:event_productsTableMouseClicked

