    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        String menuType = typeTextField.getSelectedItem().toString();
        String availability = availabilityTextField.getSelectedItem().toString();
        try {
            Class.forName("com.mysql.jdbc.Driver"); //load the driver
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/system_pos", "root", ""); //establishes the connection

            Statement stmt = conn.createStatement();
            String query = String.format("UPDATE menu SET menu_name='%s', menu_type='%s', availability='%s', price=%f WHERE menu_id= %d", nameTextField.getText(), menuType, availability, Float.parseFloat(priceTextField.getText()), Integer.parseInt(idTextField.getText()));
            stmt.executeUpdate(query);
            JOptionPane.showMessageDialog(this, "Updated");
            displayProducts();
        } catch (Exception e) {
            System.out.println(e);
        }

    }//GEN-LAST:event_updateButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/system_pos", "root", "");

            Statement stmt = conn.createStatement();
            String query = String.format("DELETE FROM menu WHERE menu_id = %d", Integer.parseInt(idTextField.getText()));
            stmt.execute(query);
            JOptionPane.showMessageDialog(this, "Menu Deleted");
            displayProducts();
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

